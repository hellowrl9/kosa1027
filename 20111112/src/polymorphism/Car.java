package polymorphism;

public class Car {
	Tire backRightTire = new Tire("뒤오른쪽", 6);
	Tire frontLeftTire = new Tire("앞왼쪽", 2);
	Tire frontRightTire = new Tire("앞오른쪽", 3);
	Tire backLeftTire = new Tire("뒤왼쪽", 4);
	
	int run() {
		System.out.println("[자동차가 달립니다.]");
		if(frontLeftTire.roll()==false) {stop();return 1;}
		if(backRightTire.roll()==false) {stop();return 2;}
		if(frontLeftTire.roll()==false) {stop();return 3;}
		if(backRightTire.roll()==false) {stop();return 4;}
		return 0;
	}
	void stop() {
		System.out.println("[자동차가 멈춥니다.]");
	}
	
}
