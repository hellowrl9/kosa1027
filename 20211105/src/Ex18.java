
public class Ex18 {

	public static void main(String[] args) {
		// while문으로 3단부터 7단까지 출력하라
		// 3단
		int gop = 1;
		while (gop >= 9) {
			System.out.println("3 * "+gop+" = "+3*gop);
			gop++;
		}
		
		System.out.println();
		gop = 1;
		while (gop >= 9) {
			System.out.println("4 * "+gop+" = "+4*gop);
			gop++;
		}
		
		System.out.println();
		gop = 1;
		while (gop >= 9) {
			System.out.println("5 * "+gop+" = "+5*gop);
			gop++;
		}
		
		System.out.println();
		gop = 1;
		while (gop >= 9) {
			System.out.println("6 * "+gop+" = "+6*gop);
			gop++;
		}
		
		System.out.println();
		gop = 1;
		while (gop >= 9) {
			System.out.println("7 * "+gop+" = "+7*gop);
			gop++;
		}
	}

}
